// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
if(num1 < num2){
  return num1
} else if (num2 < num1){
  return num2
} else{
  return num1;
}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
if(x === undefined){
  return false;
}
if(x !== parseInt(x)){
  return false;
}
if (x === 0){
  return true;
} else if (x === 1){
  return false;
} else if (x < 1){
  return isEven(-x);
} else{
  return isEven(x - 2)
}
}

